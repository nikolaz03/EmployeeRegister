    private void prikazSvih_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prikazSvih_BtnActionPerformed
        Session session = HibernateUtil.createSessionFactory().openSession();
        Transaction tx = null;

        try {
            tx = session.beginTransaction();

            // Radnik radnik = null;
            List<Radnik> list = new ArrayList();

            String hql = "from Radnik";

            Query query = session.createQuery(hql);

            list = query.list();

            DefaultListModel listModel = new DefaultListModel();

            for (int i = 0; i < list.size(); i++) {
                listModel.addElement(list.get(i));
            }
            jList1.setModel(listModel);

            tx.commit();
        } catch (HibernateException e) {
            if (tx != null) {
                tx.rollback();
            }
            System.out.println(e);
        } finally {
            HibernateUtil.close();
        }
    }//GEN-LAST:event_prikazSvih_BtnActionPerformed

