    private void unesiBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_unesiBtnActionPerformed
        Session session = HibernateUtil.createSessionFactory().openSession();
        Transaction tx = null;

        try {

            tx = session.beginTransaction();

            Radnik radnik = new Radnik(unosIme.getText(), Integer.valueOf(unosGodine.getText()), Integer.valueOf(unosDohodak.getText()), unosAdresa.getText());

            session.persist(radnik);
            //  JOptionPane.showMessageDialog(new JFrame(), "Unos podataka o zaposlenom je uspesno obavljen!");

            tx.commit();

        } catch (HibernateException e) {
            if (tx != null) {
                tx.rollback();
            }
            System.out.println(e);
        }
    }//GEN-LAST:event_unesiBtnActionPerformed

    private void izmeniBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_izmeniBtnActionPerformed
        Session session = HibernateUtil.createSessionFactory().openSession();
        Transaction tx = null;

        try {

            tx = session.beginTransaction();

            Radnik radnik = (Radnik) session.load(Radnik.class, Integer.valueOf(izmenaTrenutni_ID.getText()));

            radnik.setAdresa(izmenaAdresa.getText());
            radnik.setGodine(Integer.valueOf(izmenaGodine.getText()));
            radnik.setDohodak(Integer.valueOf(izmenaIme.getText()));
            radnik.setIme(izmenaIme.getText());

            session.merge(radnik);

            tx.commit();
        } catch (HibernateException e) {
            if (tx != null) {
                tx.rollback();
            }
            System.out.println(e);
        }
    }//GEN-LAST:event_izmeniBtnActionPerformed

